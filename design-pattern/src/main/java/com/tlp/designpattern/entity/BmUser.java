package com.tlp.designpattern.entity;

/**
 * @className: BmUser
 * @description:
 * @author: tianlingpeng
 * @create: 2019-09-30 14:21
 */
public class BmUser {
    private String gid;
    private int age;
    private String name;

    public String getGid() {
        return gid;
    }

    public void setGid(String gid) {
        this.gid = gid;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
